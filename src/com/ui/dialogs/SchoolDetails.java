    public void loadDetails() {
        if (School.isSingedUp(userName)) {
            jLabel5.setIcon(Commons.getImage(school.getRoundImage(), 50, 50));
            scl_name.setText(school.getName());
            scl_address.setText(school.getAddress());
            scl_email.setText(school.getEmail());
            scl_emailpsw.setText(school.getEmailPassword());
            scl_district.setText(school.getDistrict());
            telephone.setText(school.getTelephone());
            school_logo.setIcon(CommonUi.getVertFitImage(school.getBadge(), 100));
            school_image.setIcon(CommonUi.getVertFitImage(school.getProfileImage(), 100));
            setDimentions();
        }
    }

    public void updateDetails() {
        school.setName(scl_name.getText());
        school.setAddress(scl_address.getText());
        school.setEmail(scl_email.getText());
        school.setEmailPassword(scl_emailpsw.getText());
        school.setDistrict(scl_district.getText());
        school.setTelephone(telephone.getText());
        school.setProfileImage((ImageIcon) school_image.getIcon());
        school.setLogo((ImageIcon) school_logo.getIcon());
        setDimentions();
        school.updateDetails();
    }

    private void setDimentions() {
        Icon img = school_image.getIcon();
        if (img != null) {
            jLabel3.setText(img.getIconWidth() + "x" + img.getIconHeight());
        }
        img = school_logo.getIcon();
        if (img != null) {
            jLabel7.setText(img.getIconWidth() + "x" + img.getIconHeight());
        }
    }
    
