    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        JFileChooser jfc = chooseImage();
        String path = jfc.getCurrentDirectory().getAbsolutePath();
        switch (jfc.showDialog(null, "Select image")) {
            case JFileChooser.APPROVE_OPTION: {
                File f = jfc.getSelectedFile();
                String imgPath = f.getAbsolutePath();
                ImageIcon img = new ImageIcon(imgPath);
                school_image.setIcon(CommonUi.getVertFitImage(img, 100));
                setDimentions();
                jTextField1.setText(imgPath);
                Commons.setDefault(Defaults.MD_ImagePath, path, CONN);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

